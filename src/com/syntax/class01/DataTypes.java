package com.syntax.class01;

public class DataTypes {

	public static void main(String[] args) {
		
		//to create a variable: datatype and name
		
		//whole numbers 
		
		byte box1=21;
		
		short box2=2022;
		
		int box3=76666; //mostly will be used
		
		long box4=45678900976453524L;
		
		//decimal values
		float container1=1.99F;
		double container2=89.99;
		
		// to represent 1 character
		
		char gender='m';
		char dollar='$';
		
		//to represent tru or false (yes or no)
		
		boolean hungry=true;
		boolean sleepy=false;
		
		//how to access value from variables?
		
		System.out.println(container2);//do not use double quotes "" to access variables
		
		//access and print values from all variables
		
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		System.out.println(container1);
		System.out.println(container2);
		System.out.println(gender);
		System.out.println(dollar);
		System.out.println(hungry);
		System.out.println(sleepy);
		
		
		

		
		
		
	}

}
