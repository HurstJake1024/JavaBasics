package com.syntax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {

		//I need to print number from 1 - 90
		
		for (int i=1; i<90; i++) {
			System.out.println(i+" ");
		}
			/*
			 * start point
			 * end point
			 * increment/decrement
			 */
			
		for(int i=60; i>=10; i--) {
			 
				System.out.println(i+" ");
			}
		}

	}


